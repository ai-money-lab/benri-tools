def main():
    # Preserve already-posted items from old queue
    old_posted = {}
    if os.path.exists(QUEUE_PATH):
        with open(QUEUE_PATH, 'r', encoding='utf-8') as f:
            old_queue = json.load(f)
        for p in old_queue:
            if p.get('posted'):
                old_posted[p['id']] = p.get('posted_at', 'unknown')

    # Build new queue
    queue = []
    for post in POSTS:
        entry = {
            'id': post['id'],
            'body': post['body'],
            'reply': post['reply'],
            'posted': post['id'] in old_posted,
        }
        if entry['posted']:
            entry['posted_at'] = old_posted[post['id']]
        queue.append(entry)

    with open(QUEUE_PATH, 'w', encoding='utf-8') as f:
        json.dump(queue, f, ensure_ascii=False, indent=2)

    posted = sum(1 for p in queue if p.get('posted'))
    remaining = len(queue) - posted
    print(f'Queue regenerated: {len(queue)} posts ({posted} posted, {remaining} remaining)')
    print()

    # Show samples
    shown = 0
    for p in queue:
        if not p.get('posted') and shown < 3:
            chars = len(p['body'])
            print(f'--- {p["id"]} ({chars}字) ---')
            print(p['body'])
            print(f'[Reply]: {p["reply"][:100]}')
            print()
            shown += 1


if __name__ == '__main__':
    main()
